public class whileDemostracion {
    public static void main(String[] args) {
        
        int mesasPorLimpiar = 3;

        while (mesasPorLimpiar > 0) {
            System.out.println("Faltan " + mesasPorLimpiar + " mesas por limpiar.");
            mesasPorLimpiar--;
        }

    System.out.println("Todo Limpio.");
    }
}
