public class DivisionEntera {
    public static void main(String[] args) {
        
        int a = 7;
        int b = 2;

        System.out.println(a/b); // 3 (division entera: corta decimales)

        double c = 7.0 /2 ; // 3.5 (uno es decimal)

        System.out.println(c); // 3.5

        System.out.println(7.0/2); // 3.5

        System.out.println((double)7/2); // 3.5


    }
}