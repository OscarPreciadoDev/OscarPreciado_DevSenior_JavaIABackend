public class Script3_BloqueBase {                 // public: accesible desde cualquier lugar
                                         // class: define una clase
                                         // Script3: nombre de la clase
                                         // {: inicio del bloque de la clase

    public static void main(String[] args) {
        // public: método accesible desde cualquier lugar
        // static: pertenece a la clase, no a un objeto
        // void: no devuelve ningún valor
        // main: punto de entrada del programa
        // String[]: arreglo de cadenas de texto
        // args: nombre del parámetro que recibe argumentos
        // {: inicio del bloque del método

        System.out.println("Hola Mundo");
        // System: clase estándar de Java
        // . : operador de acceso a miembros
        // out: flujo de salida estándar (consola)
        // . : operador de acceso a miembros
        // println: imprime texto y luego hace un salto de línea
        // ("Hola Mundo"): argumento enviado al método
        // ; : indica el final de la instrucción

    }                                    // cierra el método main

}                                        // cierra la clase HolaMundo