public class N04BloqueDeClase {
    // class: declarar que se esta definiendo una nueva clase
    // Script4: Nombre de la clase


    // Un programa de Java empiza con una llamada a main().
    public static void main (String args[]){

        // main(): Es una subrutina o metodo, linea donde el programa empieza a ejecutarse. Toda aplicacion de Java empieza a ejecutarse mediante una llamada a main()
        // public: palabra clave, de un especificador de acceso, determina de que otras partes del programa pueden acceder a los miembros de la clase
        // static: palabra clave, permite que main sean llamado por el interprete de Java antes de que se haga cualquier objeto
        // void: palabra clave, que indica al compilador que main() no regresa ningun valor
        // String args[]: argumentos del metodo main(), con el parametro 'args'. se trata de una matriz de objetos del tipo String
        // printLn(): Metodo integrado para imprimir la informacion pasada como argumento
        // System.out: Se descompone en los siguientes keywords
        // System: Clase predefinida que permite acceso al sistema
        // out: flujo de salida conectado a la consola

        System.out.println("Java dirige Web");
    }
}