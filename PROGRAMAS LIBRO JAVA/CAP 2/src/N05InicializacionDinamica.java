
public class N05InicializacionDinamica {
    
    public static void main(String[] args) {
        
        double radio = 4, altura = 5;

        // Inicializa dinamicamente el volumen
        double volumen = 3.1416 * radio * radio * altura;

        System.out.println("El volumen es " + volumen); // El volumen es 251.328 
    }

}
