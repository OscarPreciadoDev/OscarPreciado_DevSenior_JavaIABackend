// Uso de modelado

public class N17UsoDeModelado {
    public static void main(String[] args) {
        int i;

        for(i = 0; i < 5; i++) {
            System.out.println(i + " / 3: " + 1/3);
            System.out.println(i + " / 3 con fracciones: " +  (double) i/3);
            System.out.println();
        }
    }


}
