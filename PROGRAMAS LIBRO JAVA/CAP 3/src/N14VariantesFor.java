// Mover afuera de bucle for

public class N14VariantesFor {
    public static void main(String[] args) {
        
        int i;

        i = 0; // Saca la inicializacion del bucle

        for(;i<10;) {
            System.out.println("Paso #" + i);
            i++; // Incrementa la variable de control
        }

    }
}
